package com.model;

public class Reward {
private int uid;
private int id;
private int cid;
private String rank;
private int aid;
private int bal;
public int getUid() {
	return uid;
}
public void setUid(int uid) {
	this.uid = uid;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getCid() {
	return cid;
}
public void setCid(int cid) {
	this.cid = cid;
}
public String getRank() {
	return rank;
}
public void setRank(String rank) {
	this.rank = rank;
}
public int getAid() {
	return aid;
}
public void setAid(int aid) {
	this.aid = aid;
}
public int getBal() {
	return bal;
}
public void setBal(int bal) {
	this.bal = bal;
}
}
