package com.model;

public class Notification {
private int cuid;
private int fuid;
private String post;
public int getCuid() {
	return cuid;
}
public void setCuid(int cuid) {
	this.cuid = cuid;
}
public int getFuid() {
	return fuid;
}
public void setFuid(int fuid) {
	this.fuid = fuid;
}
public String getPost() {
	return post;
}
public void setPost(String post) {
	this.post = post;
}

}
